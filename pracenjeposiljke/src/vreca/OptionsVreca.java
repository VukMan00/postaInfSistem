    private void txtSearchCriteriaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchCriteriaKeyPressed
        // TODO add your handling code here:
        VrecaTableModel vrecaTableModel = (VrecaTableModel)tblVrece.getModel();
        List<PopisPosiljakaUSVreci> vreceModel = vrecaTableModel.getVrece();
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if(!txtSearchCriteria.getText().isEmpty()){
                List<PopisPosiljakaUSVreci> searchVrece = vreceModel.stream()
                                                .filter(vr->vr.getOznakaVrece()== Integer.parseInt(txtSearchCriteria.getText().trim()))
                                                .collect(Collectors.toList());
                tblVrece.setModel(new VrecaTableModel(searchVrece));
            }else{
                populateTable();
            }
        }
    }//GEN-LAST:event_txtSearchCriteriaKeyPressed

    private void btnCreateVrecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateVrecaActionPerformed
        // TODO add your handling code here:
        localStorage.addItemsInHashMap("operation", "CREATE");
        SaveVreca sv = new SaveVreca(null, true);
        sv.setLocationRelativeTo(null);
        sv.setVisible(true);
        localStorage.removeItemFromHashMap("operation");
        populateTable();
    }//GEN-LAST:event_btnCreateVrecaActionPerformed

    private void btnUpdateVrecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateVrecaActionPerformed
        // TODO add your handling code here:
        PopisPosiljakaUSVreci vreca = getSelectedVreca();
        if(vreca!=null){
            localStorage.addItemsInHashMap("operation", "UPDATE");
            localStorage.addItemsInHashMap("vreca", vreca);
            SaveVreca sv = new SaveVreca(null, true);
            sv.setLocationRelativeTo(null);
            sv.setVisible(true);
            localStorage.removeItemFromHashMap("operation");
            localStorage.removeItemFromHashMap("vreca");
            populateTable();
        }
    }//GEN-LAST:event_btnUpdateVrecaActionPerformed

    private void btnDeleteVrecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVrecaActionPerformed
        // TODO add your handling code here:
        PopisPosiljakaUSVreci vreca = getSelectedVreca();
        if(vreca!=null){
            try {
                Controller.getInstance().deleteVreca(vreca);
                JOptionPane.showMessageDialog(this, "Uspešno ste izbrisali vreću");
                populateTable();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Exception", JOptionPane.PLAIN_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteVrecaActionPerformed

    private void btnViewVrecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewVrecaActionPerformed
        // TODO add your handling code here:
        PopisPosiljakaUSVreci vreca = getSelectedVreca();
        if(vreca!=null){
            localStorage.addItemsInHashMap("vreca", vreca);
            PregledVrece pv = new PregledVrece(null, true);
            pv.setLocationRelativeTo(null);
            pv.setVisible(true);
            localStorage.removeItemFromHashMap("vreca");
            populateTable();
        }
    }//GEN-LAST:event_btnViewVrecaActionPerformed


