    private void populateTable() {
        try {
            vrece = Controller.getInstance().getVrece(new PopisPosiljakaUSVreci());
            tblVrece.setModel(new VrecaTableModel(vrece));
        } catch (Exception ex) {
            Logger.getLogger(OptionsVreca.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private PopisPosiljakaUSVreci getSelectedVreca() {
        int selectedRow = tblVrece.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Niste izabrali vreću");
            return null;
        }else{
            return ((VrecaTableModel) tblVrece.getModel()).getVrece().get(selectedRow);
        }
    }
